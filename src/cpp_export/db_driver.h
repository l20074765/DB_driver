    int32 billPayback;      //硬币器退币按键触发
    int32 billErr;          //纸币器故障码      0正常
    int32 billAmount;       //纸币器收币金额
    int32 billRemain;       //纸币器储币金额

    int32 coinEnable;       //硬币器使能 1使能 0禁能
    int32 coinPayback;      //硬币器退币按键触发
